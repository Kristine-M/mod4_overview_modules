def mood_response(mood):
    
    if mood == "happy":
        return "I am glad you are happy right now"
    elif mood == "sad":
        return "Here is a lollipop to make your day a little better"
    elif mood == "nervous":
        return "You can do whatever you are planning to do"
    elif mood == "tired":
        return "Me too, I am also drained"